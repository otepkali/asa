<h4>Teacher’s Casino</h4>

<p>Teacher makes a worksheet with 10 facts about me. Students must determine whether they are true or false. While choosing true or false, they must also bet on their answer. They have 100 points and must bet them all, with a minimum bet of 5 and maximum of 20. Then, teacher goes through the facts, having students raise their hands to show who answered what. If they were correct, they receive double points. If they were wrong, 0 points. Teacher may use a powerpoint as a visual aid when reviewing the facts. Recommended time 5-10 minutes.</p>
<h4>Yes/No Game</h4>

<p>Teacher explains and models that students are going to ask yes or no questions. Teacher elicits some yes/no forms (Are you…? Do you…? Can you…?) and write them on the board. Students are not allowed to ask other types of questions, and teacher should not answer if they do so. Recommended time 5-7 minutes.</p>

<h4>Signatures</h4>
<p>
Teacher asks students to stand up and form a circle. Then teacher develops the idea of uniqueness amongst the group by asking each person to think of a signature movement, action or sound that is unique to them. It needn't be anything complicated; it can be a hand clap, a step or two sideways, a spin, a bow, a gesture, a mime - something that isn't copied or too similar. Teacher leads the way with his/her own name and signature. The person next to the teacher says his/her name and gesture and so on right round the circle. Everyone should have their own little movement or sound. Next, the students try to remember them all one by one. Recommended time 5-10 minutes.</p>

<h4>Find someone who</h4>

<p>This is a mingling activity, where teacher provides students with the cards with set questions (e.g. Find someone who doesn’t like pizza, etc.). Students move around and talk to other students in order to find ones that meet a certain criteria (i.e., ones who can answer certain questions). For higher-level or older students, teacher may leave some blank space so they can make their own questions. When everyone has gotten a chance to look over the cards, students can walk, talk and write the answers on the cards. Recommended time 5-10 minutes.</p>

